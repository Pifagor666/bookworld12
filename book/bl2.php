                        <h2>Современные технологии книжного производства</h2>
                            <p>Сегодня при изготовлении книг чаще всего используется технология офсетной печати или литографии, при которой краска с печатающей формы переносится на бумагу через промежуточный цилиндр.
                                Страницы последовательно распечатываются на рулонной бумаге, которая впоследствии разделяется на отдельные листы, таким образом, что в итоге книга сразу будет собрана в правильном порядке.
                                Такой станок, соответственно, называется рулонным, или ротационным; существуют также и офсетные станки, печатающие на отдельных листах, а не на рулонах.</p>
                            <p>После сбора листов с оттисками осуществляются переплётные работы.
                                В середине прошлого века соответствующая часть работы выполнялась обособленными предприятиями, которые не занимались книгопечатанием и выполняли только лишь работу по переплёту книг; в настоящее время активные процессы слияния и реорганизации компаний приводят к тому, что интеграция различных задач в рамках одной и той же издательской фирмы нарастает, и теперь найти предприятие, подобное описанному выше, затруднительно.
                                Растёт популярность переплёта книг без традиционной прошивки, однако прежние методы брошюрования также находят применение в полиграфическом процессе.</p><br>
                            <p><img src="images/blog/bl2.jpg" width="75%"></p><br>                                
                            <p>Для современного книгопечатания характерна стандартизация: изготавливаемые книги имеют, как правило, определённый размер и формат.
                                В англоговорящих странах, за исключением США, доминируют британские стандарты; собственные нормы и правила действуют в странах Европы.
                                Бумага также изготавливается специально для полиграфических нужд; для облегчения процесса чтения она традиционно делается не чисто белой, а слегка затемнённой, а также имеет определённую плотность, чтобы напечатанный на одной стороне листа текст не просвечивал с другой стороны.
                                В зависимости от конкретного типа книги применяется бумага определённого качества; наиболее распространённой разновидностью является мелованная бумага того или иного сорта.</p>
                            <br>
                        <h2>Цифровая печать</h2>
                            <p>Помимо прочего, на современном этапе книги изготавливаются также и методом цифровой печати.
                                При этом страницы формируются примерно тем же способом, что и документы, печатаемые офисной техникой — лазерными принтерами или копировальными аппаратами, с использованием тонера, а не чернил.
                                Такой подход позволяет печатать малые тиражи изданий (до нескольких сотен экземпляров), в том числе благодаря отсутствию подготовительных этапов, необходимых в работе с офсетным станком.
                                Появление цифровой печати содействовало формированию нового подхода к книгоизданию — т. н. печати по требованию, когда копии книг изготавливаются специально для конкретного клиента, уже после того, как он оформит заказ на то или иное издание.</p><br>
                            <p><img src="images/blog/bl2.1.jpg" width="75%"></p>                       
                            <br>
                        <h2>Электронная книга</h2>
                            <p>Термин «электронная книга» (также e-книга, англ. e-book) применяется по отношению к книге, содержащей информацию обычной книги, но представленную в цифровом формате и размещённой на электронном носителе (CD-ROM, DVD-ROM и пр.).</p>
                            <p>Нарастающее число публикаций (так называемый информационный взрыв) поставил перед библиотеками вопрос хранения столь большого массива информации.
                                Появление электронной публикации и сети Интернет позволяет не печатать информацию в виде бумажных книг, а хранить её на гораздо более ёмких электронных носителях в электронных библиотеках, предоставляя доступ к ней как локально, так и дистанционно — через Всемирную сеть.</p>                        
                            <p>Книги в таком виде имеют как свои плюсы, так и минусы, но, в связи с некоторым неудобством существующих систем чтения электронных книг (например, скорость чтения электронных книг, согласно исследованию «Nielsen Norman Group», уступает в среднем на 10 % скорости чтения их бумажных аналогов[35]), зачастую отдаётся предпочтение традиционным бумажным, поддерживая их производство на достаточном уровне.</p>
                            <p>Тем не менее, в настоящее время многие стараются оцифровать имеющиеся книги, чтобы обеспечить удобство хранения их на электронных носителях и беспрепятственный доступ к ним всех желающих.
                            Также есть наработки и для процесса их публикации, например, печать по требованию, электронные журналы и т. п.</p>
                            <br>
                        <h2>Аудиокнига</h2>                            
                            <p>Для создания аудиокниги текст того или иного произведения начитывается человеком — как правило, профессиональным актёром либо группой таковых — или (реже) синтезатором речи, после чего записывается на какой-либо звуковой носитель.
                                К тексту может также добавляться определённое шумовое или музыкальное сопровождение.
                                Аудиокниги обладают как преимуществами (к примеру, возможность восприятия информации людьми, по той или иной причине не способными к чтению), так и недостатками (в частности, звуковой файл существенно более объёмен, нежели собственно текст).</p><br>                         
