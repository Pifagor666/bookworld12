	    		<h2>О компании</h2>
	    			<p>Сегодня при изготовлении книг чаще всего используется технология офсетной печати или литографии, при которой краска с печатающей формы переносится на бумагу через промежуточный цилиндр.
                        Страницы последовательно распечатываются на рулонной бумаге, которая впоследствии разделяется на отдельные листы, таким образом, что в итоге книга сразу будет собрана в правильном порядке.
                        Такой станок, соответственно, называется рулонным, или ротационным; существуют также и офсетные станки, печатающие на отдельных листах, а не на рулонах.</p>
					<p>После сбора листов с оттисками осуществляются переплётные работы.
                        В середине прошлого века соответствующая часть работы выполнялась обособленными предприятиями, которые не занимались книгопечатанием и выполняли только лишь работу по переплёту книг; в настоящее время активные процессы слияния и реорганизации компаний приводят к тому, что интеграция различных задач в рамках одной и той же издательской фирмы нарастает, и теперь найти предприятие, подобное описанному выше, затруднительно.
                        Растёт популярность переплёта книг без традиционной прошивки, однако прежние методы брошюрования также находят применение в полиграфическом процессе.</p><br>
                    <p><img src="images/blog/1.jpg" width="100%"></p><br>
                    <p>Для современного книгопечатания характерна стандартизация: изготавливаемые книги имеют, как правило, определённый размер и формат.
                        В англоговорящих странах, за исключением США, доминируют британские стандарты; собственные нормы и правила действуют в странах Европы.
                        Бумага также изготавливается специально для полиграфических нужд; для облегчения процесса чтения она традиционно делается не чисто белой, а слегка затемнённой, а также имеет определённую плотность, чтобы напечатанный на одной стороне листа текст не просвечивал с другой стороны.
                        В зависимости от конкретного типа книги применяется бумага определённого качества; наиболее распространённой разновидностью является мелованная бумага того или иного сорта.</p>
					<p>В зависимости от методов книжного производства — типографской печати, формы, типа и/или содержания, книги могут иметь различный внешний вид, составные части и структуру.
                        В основном книги включают в себя следующие элементы:</p>
                    <ol>
                        <li>книжный оклад;</li>
                        <li>суперобложка — ламинированная бумажная обложка поверх переплёта книги;</li>
                        <li>переплёт (твёрдый) — прочная, обычно твёрдая, крышка из картона, кожи, ткани, бумаги, в которую заключаются (вклеиваются) сброшюрованные листы книги;</li>
                        <li>обложка или «мягкий переплёт» — мягкая или полутвёрдая обложка на которой указывается наименование и авторство книги;</li>
                        <li>основу переплёта составляют:</li>
                        <ol>
                            <li>плотная бумага;</li>
                            <li>картон;</li>
                            <li>натуральная и/или искусственная кожа;</li>
                            <li>ткань;</li>
                        </ol>
                        <li>тиснение:</li>
                        <ol>
                            <li>сусальное золото;</li>
                            <li>поталь;</li>
                            <li>цветная фольга;</li>
                            <li>бронзовая фольга;</li>
                            <li>алюминиевая фольга;</li>
                            <li>«юбилейная» переплётная фольга;</li>
                        </ol>
                    </ol>
